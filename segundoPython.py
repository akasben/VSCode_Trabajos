n1=int(input('Ingrese el primer número:'))
n2=int(input('Ingrese el segundo número:'))

suma=n1+n2

resta=n2-n1

producto=n1*n2

potencia=n1**n2

cuadradoperfecto=n1**2+2*(n1*n2)+n2**2

total=suma+resta+potencia+producto+cuadradoperfecto

print('Los resultados son: ',suma,', ', resta,', ',producto,', ',potencia,', ',cuadradoperfecto,', ',total,'.')